package com.spring.ex;

public class Calculator {

	public double add(double f, double s) {
		return f + s;
	}

	public double subtract(double f, double s) {
		return f - s;
	}

	public double multiply(double f, double s) {
		return f * s;
	}

	public double divide(double f, double s) {
		return f / s;
	}

}
